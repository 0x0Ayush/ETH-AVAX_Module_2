let contractInstance;
let accounts;

async function loadWeb3() {
  if (window.ethereum) {
    window.web3 = new Web3(window.ethereum);
    await window.ethereum.enable();
  } else if (window.web3) {
    window.web3 = new Web3(window.web3.currentProvider);
  } else {
    alert("Non-Ethereum browser detected. Please install MetaMask extension.");
  }
}

async function loadContract() {
  contractInstance = new web3.eth.Contract(contractABI, contractAddress);
  accounts = await web3.eth.getAccounts();
}

function createENS() {
  const ensName = document.getElementById("ensName").value;
  contractInstance.methods.createENS(ensName).send({ from: accounts[0] })
    .then(() => alert("ENS Domain created successfully!"))
    .catch(error => alert("Error: " + error));
}

function listENSForSale() {
  const tokenId = document.getElementById("tokenId").value;
  const price = web3.utils.toWei(document.getElementById("price").value, 'ether');
  contractInstance.methods.listENSForSale(tokenId, price).send({ from: accounts[0] })
    .then(() => alert("ENS Domain listed for sale successfully!"))
    .catch(error => alert("Error: " + error));
}

function transferENS() {
  const tokenId = document.getElementById("transferTokenId").value;
  const toAddress = document.getElementById("transferTo").value;
  contractInstance.methods.transferFrom(accounts[0], toAddress, tokenId).send({ from: accounts[0] })
    .then(() => alert("ENS Domain transferred successfully!"))
    .catch(error => alert("Error: " + error));
}

function getENSCount() {
  contractInstance.methods.balanceOf(accounts[0]).call()
    .then(count => document.getElementById("ensCount").innerText = `You hold ${count} ENS Domains.`)
    .catch(error => alert("Error: " + error));
}

function getENSInfo() {
  const tokenId = document.getElementById("ensTokenId").value;
  contractInstance.methods.getTokenURI(tokenId).call()
    .then(tokenURI => document.getElementById("ensInfo").innerText = `Token ID: ${tokenId}, Token URI: ${tokenURI}`)
    .catch(error => alert("Error: " + error));
}

function getContractInfo() {
  contractInstance.methods.name().call()
    .then(name => document.getElementById("contractName").innerText = `Contract Name: ${name}`)
    .catch(error => alert("Error: " + error));

  contractInstance.methods.symbol().call()
    .then(symbol => document.getElementById("contractSymbol").innerText = `Contract Symbol: ${symbol}`)
    .catch(error => alert("Error: " + error));

  contractInstance.methods.owner().call()
    .then(owner => document.getElementById("contractOwner").innerText = `Contract Owner: ${owner}`)
    .catch(error => alert("Error: " + error));
}

window.onload = async () => {
  await loadWeb3();
  await loadContract();
  getContractInfo();
};
